    public void editarVenda(int codigo) {
        Venda entidade;
        if (codigo == 0) {
            java.util.Date dataUtil = new java.util.Date();
            java.sql.Date dataSql = new java.sql.Date(dataUtil.getTime());
            entidade = new Venda(0, null, dataSql, new BigDecimal(0));
        } else {
            entidade = dao.Abrir(codigo);
        }
        try {
            editar = new TelaEditarVenda();
            editar.setEntidade(entidade);
            editar.setListagem(this);
            this.getParent().add(editar);
            editar.setVisible(true);
            this.setVisible(false);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

